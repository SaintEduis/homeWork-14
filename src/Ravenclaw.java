public class Ravenclaw {

    private int intelligence;
    private int wisdom;
    private int wit;
    private int creativeStreak;

    public Ravenclaw(int intelligence, int wisdom, int wit, int creativeStreak) {
        this.intelligence = intelligence;
        this.wisdom = wisdom;
        this.wit = wit;
        this.creativeStreak = creativeStreak;
    }
}
